public class MyProvinceAdapter extends BaseAdapter {
    private String[] str;
    private Context context;

    public MyProvinceAdapter(String[] str, Context context) {
        this.str = str;
        this.context = context;
    }

    @Override
    public int getCount() {
        return str.length;
    }

    @Override
    public Object getItem(int position) {
        return str[position];
    }

    @Override
    public long getItemId(int position) {
        return 0;
    }

    @Override
    public View getView(int position, View convertView, ViewGroup parent) {
        Holder holder;
        if (convertView == null) {
            holder = new Holder();
            convertView = LayoutInflater.from(context).inflate(R.layout.province_item, null);
            holder.textView = (TextView) convertView.findViewById(R.id.province_item_text);
            convertView.setTag(holder);
        } else {

            holder = (Holder) convertView.getTag();
        }
        String s = str[position];
        holder.textView.setText(s);

        return convertView;
    }

    class Holder {
        private TextView textView;

    }
}
